#import "demoUrlArgumentsFilter.h"

@implementation demoUrlArgumentsFilter {
    NSDictionary *_arguments;
}

+ (demoUrlArgumentsFilter *)filterWithArguments:(NSDictionary *)arguments {
    return [[self alloc] initWithArguments:arguments];
}

- (id)initWithArguments:(NSDictionary *)arguments {
    self = [super init];
    if (self) {
        _arguments = arguments;
    }
    return self;
}

- (NSString *)filterUrl:(NSString *)originUrl withRequest:(YTKBaseRequest *)request {
    return [self urlStringWithOriginUrlString:originUrl appendParameters:_arguments];
}

- (NSString *)urlStringWithOriginUrlString:(NSString *)originUrlString appendParameters:(NSDictionary *)parameters {
    NSString *paraUrlString = AFQueryStringFromParameters(parameters);
    
    if (!(paraUrlString.length > 0)) {
        return originUrlString;
    }
    
    BOOL useApiUrl = NO;
    static NSString *apiUrl = nil;
    NSURLComponents *components = [NSURLComponents componentsWithString:originUrlString];
    if (!components) {
        useApiUrl = YES;
        if (!apiUrl) {
            apiUrl = APP_TRUE_SERVER_URL;
        }
        components = [NSURLComponents componentsWithString:apiUrl];
    }
    
    NSString *queryString = components.query ?: @"";
    NSString *newQueryString = [queryString stringByAppendingFormat:queryString.length > 0 ? @"&%@" : @"%@", paraUrlString];
    
    components.query = newQueryString;
    
    if (useApiUrl) {
        return [components.URL.absoluteString substringFromIndex:apiUrl.length - 1];
    } else {
        return components.URL.absoluteString;
    }
}

@end
