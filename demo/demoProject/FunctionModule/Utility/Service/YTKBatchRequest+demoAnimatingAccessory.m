#import "YTKBatchRequest+demoAnimatingAccessory.h"
#import "demoAnimatingRequestAccessory.h"

@implementation YTKBatchRequest (demoAnimatingAccessory)

- (demoAnimatingRequestAccessory *)animatingRequestAccessory {
    for (id accessory in self.requestAccessories) {
        if ([accessory isKindOfClass:[demoAnimatingRequestAccessory class]]){
            return accessory;
        }
    }
    return nil;
}

- (NSString *)animatingText {
    return self.animatingRequestAccessory.animatingText;
}

- (void)setAnimatingText:(NSString *)animatingText {
    if (!self.animatingRequestAccessory) {
        [self addAccessory:[demoAnimatingRequestAccessory accessoryWithAnimatingText:animatingText]];
    } else {
        self.animatingRequestAccessory.animatingText = animatingText;
    }
}

@end
