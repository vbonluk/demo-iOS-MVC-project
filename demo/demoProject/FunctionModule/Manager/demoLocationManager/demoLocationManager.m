#import "demoLocationManager.h"

static demoLocationManager* _instance = nil;

@interface demoLocationManager ()<BMKLocationAuthDelegate,BMKLocationManagerDelegate>

@property (strong, nonatomic) BMKLocationManager *locationManager;

@end

@implementation demoLocationManager

- (instancetype)init{
    if ((self = [super init])) {
        
        [[BMKLocationAuth sharedInstance] checkPermisionWithKey:@"demo key" authDelegate:self];
    }
    return self;
}

+ (instancetype) shareInstance{
    static dispatch_once_t onceToken ;
    dispatch_once(&onceToken, ^{
        _instance = [[self alloc] init];
    }) ;
    
    return _instance ;
}

/**
 单次定位
 如果当前正在连续定位，调用此方法将会失败，返回NO。
 */
- (void)startSingleLocationWithCompletionBlock:(void(^)(CLLocation *location))completionBlock{
    [self.locationManager requestLocationWithReGeocode:NO withNetworkState:NO completionBlock:^(BMKLocation * _Nullable location, BMKLocationNetworkState state, NSError * _Nullable error) {
        if (completionBlock) {
            completionBlock(location.location);
        }
    }];
}

- (BMKLocationManager *)locationManager{
    if (!_locationManager){
        //初始化实例
        _locationManager = [[BMKLocationManager alloc] init];
        //设置delegate
        _locationManager.delegate = self;
        //设置返回位置的坐标系类型
        _locationManager.coordinateType = BMKLocationCoordinateTypeBMK09LL;
        //设置距离过滤参数
        _locationManager.distanceFilter = kCLDistanceFilterNone;
        //设置预期精度参数
        _locationManager.desiredAccuracy = kCLLocationAccuracyBest;
        //设置应用位置类型
        _locationManager.activityType = CLActivityTypeAutomotiveNavigation;
        //设置是否自动停止位置更新
        _locationManager.pausesLocationUpdatesAutomatically = NO;
        //设置是否允许后台定位
        //_locationManager.allowsBackgroundLocationUpdates = YES;
        //设置位置获取超时时间
        _locationManager.locationTimeout = 10;
        //设置获取地址信息超时时间
        _locationManager.reGeocodeTimeout = 10;
    }
    return _locationManager;
}

@end
